<script>
import Modal from "./Modal.vue";
import { mapState } from "vuex";
export default {
  name: "ResetGameModal",
  components: { Modal },
  data() {
    return {
      options: {
        roles: true,
        bluffs: true,
        reminders: true,
        deathStatus: true,
        nominationStatus: true,
        aliveStatus: true,
        gameHistory: true,
        voteHistory: true,
        roundInfo: true,
        voteSettings: true,
        nightSettings: true,
        markerSettings: true,
        seatOccupation: true,
        playerIds: true,
      },
    };
  },
  computed: {
    ...mapState(["session"]),
    isStoryteller() {
      return !this.session || this.session.isSpectator === false;
    },
    hasSelected() {
      return Object.values(this.filteredOptions).some(Boolean);
    },
    filteredOptions() {
      // è¿”å›ä¸€ä¸ªæ–°å¯¹è±¡ï¼Œç©å®¶ç«¯å»æ‰å¤šä½™å±æ€§ï¼Œä¿è¯æ‰€æœ‰å£°æ˜éƒ½è¢«ç”¨åˆ°
      const opts = { ...this.options };
      if (!this.isStoryteller) {
        delete opts.voteHistory;
        delete opts.roundInfo;
        delete opts.seatOccupation;
        delete opts.playerIds;
      }
      return opts;
    },
  },
  methods: {
    selectAll() {
      Object.keys(this.filteredOptions).forEach(
        (key) => (this.options[key] = true),
      );
    },
    selectNone() {
      Object.keys(this.filteredOptions).forEach(
        (key) => (this.options[key] = false),
      );
    },
    confirmReset() {
      if (!this.hasSelected) return;
      this.$emit("confirm", { ...this.filteredOptions });
      this.$emit("close");
    },
    closeModal() {
      this.$emit("close");
    },
  },
};
</script>

